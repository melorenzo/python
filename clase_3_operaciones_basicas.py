
numero1 = input("Ingrese un número: ")
numero2 = input("Ingrese otro número: ")

print("Vamos a realizar las cuatro operaciones básicas. Por favor presione Enter para continuar...")


suma = int(numero1) + int(numero2)
resta = int(numero1) - int(numero2)
multiplicacion = int(numero1) * int(numero2)
modulo = int(numero1) % int(numero2)


print("El resultado de la suma es:", suma)
print("El resultado de la resta es:", resta)
print("El resultado de la multiplicación es:", multiplicacion)
print("El resultado del módulo es:", modulo)
